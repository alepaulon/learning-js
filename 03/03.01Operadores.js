/*OPERADORES ARITMÉTICOS*/

let a = 3, b = 2;

//Suma
let z = a + b;
console.log("Resultado de la suma: " + z);

//Resta
z = a - b;
console.log(z);

//Multiplicación

z = a * b;
console.log(z);

//División

z = a / b;
console.log(z);

//Residuo de división

z = a % b;
console.log(z);

//Exponente

z = a ** b;
console.log(z);

z = a + b;
//Incremento
//Pre incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a);
console.log(z);

//Post incremento (el operador ++ despues de la variable)
z = b++;
console.log(b);
console.log(z);

//Decremento
z = a;

//Predecremento
z = --a;
console.log(a);
console.log(z);

//Postdecremento
z = b--;
console.log(b);
console.log(z);

